    lines.extend(data.get("final", {}).get("loon", []))
    write(ROOT / "loon" / "rules.list", "\n".join(lines).rstrip() + "\n")

    plugin = [
        "#!name=Eggy Proxy Rules",
        "#!desc=个人代理规则插件：由 source/rules.yaml 自动生成",
        "#!author=liueggy",
        "#!homepage=https://github.com/liueggy/proxy-rules",
        "#!icon=https://raw.githubusercontent.com/Koolson/Qure/master/IconSet/Color/Proxy.png",
        "",
        "[Rule]",
    ]
    for key, cat, rules in iter_categories(data):
        plugin.append(f"# {cat.get('name', key)}")
        for r in rules:
            plugin.append(rule_line(r, cat.get("policy", data["meta"].get("default_policy", "Proxy"))))
        plugin.append("")

    owner = data.get("meta", {}).get("owner", "liueggy")
    repo = data.get("meta", {}).get("repo", "proxy-rules")
    raw_base = f"https://raw.githubusercontent.com/{owner}/{repo}/main"

    qx = [
        "# AUTO-GENERATED by scripts/build.py. Edit source/rules.yaml instead.",
        "# Quantumult X 外部开源规则源",
        "# 用法：复制到 Quantumult X 配置的 [filter_remote] 区域",
        "",
        f"{raw_base}/quantumult-x/rules/direct-servers.list, tag=Eggy Direct Servers, update-interval={interval}, opt-parser=false, enabled=true",
        "",
    ]
    loon = [
        "# AUTO-GENERATED by scripts/build.py. Edit source/rules.yaml instead.",
        "# Loon 外部开源规则源",
        "# 用法：复制到 Loon 配置的 [Remote Rule] 区域",
        "",
        f"{raw_base}/loon/rules/direct-servers.list, policy=DIRECT, tag=Eggy Direct Servers, enabled=true",
        "",
    ]
    stash = [
        "# AUTO-GENERATED by scripts/build.py. Edit source/rules.yaml instead.",
        "# Stash Override：外部开源规则源",
        "# 用法：Stash → Override → 添加远程覆写",
        "",
        "name: External Rule Providers",
        "",
        "rule-providers:",
        "  eggy-custom:",
        "    type: http",
        "    behavior: classical",
        f"    url: {raw_base}/stash/rules/direct-servers.yaml",
        "    path: ./ruleset/eggy/direct-servers.yaml",
        f"    interval: {interval}",
    ]
    stash_rules = [
        "rules:",
        "  # 自己维护的直连规则优先级最高",
        "  - RULE-SET,eggy-custom,DIRECT",
        "",
    ]
    docs = [
        "# 外部开源规则源",
        "",
        "本文件由 `source/rules.yaml` 自动生成。通用大规则建议通过外部远程规则源引用，避免重复维护。",
        "",
        "## 已接入来源",
        "",
    ]
    grouped = {}
    for provider_key, provider, item_key, item, name, policy, base in iter_external_items(data):
        grouped.setdefault(provider_key, provider)
        qx_url = f"{base['qx']}/{name}/{name}.list"
        loon_url = f"{base['loon']}/{name}/{name}.list"
        stash_url = f"{base['stash']}/{name}/{name}.yaml"
        qx.append(f"{qx_url}, tag={name}, force-policy={policy}, update-interval={interval}, opt-parser=false, enabled=true")
        loon.append(f"{loon_url}, policy={policy}, tag={name}, enabled=true")
        provider_name = provider_key.replace('-', '_')

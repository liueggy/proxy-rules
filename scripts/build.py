#!/usr/bin/env python3
from pathlib import Path
import sys
import yaml

ROOT = Path(__file__).resolve().parents[1]
SOURCE = ROOT / "source" / "rules.yaml"
HEADER = "# AUTO-GENERATED by scripts/build.py. Edit source/rules.yaml instead.\n"


def load_source():
    with SOURCE.open("r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def norm_rule(item):
    if isinstance(item, str):
        parts = [p.strip() for p in item.split(",")]
        if len(parts) < 2:
            raise ValueError(f"Invalid rule string: {item}")
        return {
            "type": parts[0],
            "value": parts[1],
            "options": ",".join(parts[2:]) if len(parts) > 2 else "",
            "comment": "",
        }
    if isinstance(item, dict):
        return {
            "type": str(item["type"]).strip(),
            "value": str(item["value"]).strip(),
            "options": str(item.get("options", "")).strip(),
            "comment": str(item.get("comment", "")).strip(),
        }
    raise TypeError(f"Unsupported rule item: {item!r}")


def iter_categories(data):
    for key, cat in data.get("categories", {}).items():
        yield key, cat, [norm_rule(x) for x in cat.get("rules", [])]


def rule_line(rule, policy=None, include_policy=True, stash_payload=False):
    parts = [rule["type"], rule["value"]]
    if include_policy and policy:
        parts.append(policy)
    if rule["options"]:
        parts.extend([p for p in rule["options"].split(",") if p])
    line = ",".join(parts)
    if stash_payload:
        return f"  - {line}"
    return line


def write(path, content):
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    print(f"wrote {path.relative_to(ROOT)}")


def build_qx(data):
    lines = [HEADER.rstrip(), "# Quantumult X 分流规则", ""]
    for key, cat, rules in iter_categories(data):
        lines.append(f"# {cat.get('name', key)}")
        for r in rules:
            if r["comment"]:
                lines.append(f"# {r['comment']}")
            lines.append(rule_line(r, cat.get("policy", data["meta"].get("default_policy", "Proxy"))))
        lines.append("")
    lines.append("# Final")
    lines.extend(data.get("final", {}).get("qx", []))
    write(ROOT / "quantumult-x" / "rules.list", "\n".join(lines).rstrip() + "\n")

    for key, cat, rules in iter_categories(data):
        lines = [HEADER.rstrip(), f"# Quantumult X - {cat.get('name', key)}", ""]
        for r in rules:
            lines.append(rule_line(r, cat.get("policy", data["meta"].get("default_policy", "Proxy"))))
        write(ROOT / "quantumult-x" / "rules" / f"{key}.list", "\n".join(lines).rstrip() + "\n")


def build_loon(data):
    lines = [HEADER.rstrip(), "# Loon 分流规则", ""]
    for key, cat, rules in iter_categories(data):
        lines.append(f"# {cat.get('name', key)}")
        for r in rules:
            if r["comment"]:
                lines.append(f"# {r['comment']}")
            lines.append(rule_line(r, cat.get("policy", data["meta"].get("default_policy", "Proxy"))))
